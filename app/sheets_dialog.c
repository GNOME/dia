static GtkBuilder *
builder_new_from_file (const char *filename)
{
  GError *error = NULL;
  gchar *uifile;
  GtkBuilder *builder;

  builder = gtk_builder_new();
  uifile = build_ui_filename (filename);
  if (!gtk_builder_add_from_file (builder, uifile, &error)) {
    g_warning ("Couldn't load builder file: %s", error->message);
    g_error_free(error);
  }
  g_free (uifile);
  return builder;
}

